import UIKit

extension Int {
    
    func secondsToHours() -> Double {
        return Double(self/3600)
    }
    
    func secondsToHoursMinutesSeconds() -> (Int, Int, Int) {
        return (self / 3600, (self % 3600) / 60, (self % 3600) % 60)
    }
    
}
